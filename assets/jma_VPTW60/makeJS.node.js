const fs = require('fs');
const files = fs.readdirSync('.').filter(v => !v.endsWith('.js'));

for (const file of files) {
    const content = fs.readFileSync('./' + file, { encoding: 'utf8' });
    fs.writeFileSync('./' + file + '.js', `// FILE AUTOMATICALLY GENERATED BY makeJS.node.js; DO NOT EDIT\nJMA_VPTW60['${file}']=\`${content}\`;`);
    console.log(`<script src="./assets/jma_VPTW60/${encodeURIComponent(file)}.js"></script>`);
}
